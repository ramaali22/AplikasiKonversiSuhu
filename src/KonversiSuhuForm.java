    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
        // Ambil nilai suhu dari JTextField input
        double inputSuhu = Double.parseDouble(jTextField1.getText());
        double hasil = 0;

        // Ambil skala asal dari ComboBox
        String skalaAsal = jComboBox1.getSelectedItem().toString();

        // Tentukan skala tujuan dari RadioButton yang dipilih
        String skalaTujuan = "";
        if (jRadioButton1.isSelected()) {
            skalaTujuan = "Celsius";
        } else if (jRadioButton2.isSelected()) {
            skalaTujuan = "Fahrenheit";
        } else if (jRadioButton3.isSelected()) {
            skalaTujuan = "Kelvin";
        } else if (jRadioButton4.isSelected()) {
            skalaTujuan = "Reamur";
        }

        // Jika skala asal sama dengan skala tujuan, tampilkan langsung tanpa konversi
        if (skalaAsal.equals(skalaTujuan)) {
            jTextField2.setText(String.valueOf(inputSuhu));
            return; // Hentikan proses konversi di sini
        }

           // Konversi suhu berdasarkan skala asal dan tujuan
           switch (skalaAsal) {
               case "Celsius":
                   switch (skalaTujuan) {
                       case "Fahrenheit":
                           hasil = inputSuhu * 9 / 5 + 32;
                           break;
                       case "Kelvin":
                           hasil = inputSuhu + 273.15;
                           break;
                       case "Reamur":
                           hasil = inputSuhu * 4 / 5;
                           break;
                       default:
                           break;
                   }      break;
               case "Fahrenheit":
                   switch (skalaTujuan) {
                       case "Celsius":
                           hasil = (inputSuhu - 32) * 5 / 9;
                           break;
                       case "Kelvin":
                           hasil = (inputSuhu - 32) * 5 / 9 + 273.15;
                           break;
                       case "Reamur":
                           hasil = (inputSuhu - 32) * 4 / 9;
                           break;
                       default:
                           break;
                   }      break;
               case "Kelvin":
                   switch (skalaTujuan) {
                       case "Celsius":
                           hasil = inputSuhu - 273.15;
                           break;
                       case "Fahrenheit":
                           hasil = (inputSuhu - 273.15) * 9 / 5 + 32;
                           break;
                       case "Reamur":
                           hasil = (inputSuhu - 273.15) * 4 / 5;
                           break;
                       default:
                           break;
                   }      break;
               case "Reamur":
                   switch (skalaTujuan) {
                       case "Celsius":
                           hasil = inputSuhu * 5 / 4;
                           break;
                       case "Fahrenheit":
                           hasil = inputSuhu * 9 / 4 + 32;
                           break;
                       case "Kelvin":
                           hasil = inputSuhu * 5 / 4 + 273.15;
                           break;
                       default:
                           break;
                   }      break;
               default:
                   break;
           }

            // Tampilkan hasil konversi di JTextField hasil
            jTextField2.setText(String.valueOf(hasil));
        } catch (NumberFormatException e) {
            // Tampilkan pesan error jika input tidak valid
            JOptionPane.showMessageDialog(this, "Input tidak valid! Masukkan angka.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

