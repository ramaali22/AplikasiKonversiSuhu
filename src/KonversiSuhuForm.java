    private void lakukanKonversi() {
    try {
        
        double input = Double.parseDouble(jTextField1.getText());
        String skalaAsal = jComboBox1.getSelectedItem().toString();
        double hasil = 0;

        // Pilih skala tujuan berdasarkan JRadioButton yang dipilih
        if (jRadioButton1.isSelected()) {
            hasil = Celsius(input, skalaAsal);
        } else if (jRadioButton2.isSelected()) {
            hasil = Fahrenheit(input, skalaAsal);
        } else if (jRadioButton3.isSelected()) {
            hasil = Kelvin(input, skalaAsal);
        } else if (jRadioButton4.isSelected()) {
            hasil = Reamur(input, skalaAsal);
        }

        jTextField2.setText(String.format("%.2f", hasil));
        } catch (NumberFormatException e) {
            jTextField2.setText("Input tidak valid");
        }
    }
    
    private double Celsius(double input, String skalaAsal) {
        switch (skalaAsal) {
            case "Fahrenheit":
                return (input - 32) * 5 / 9;
            case "Kelvin":
                return input - 273.15;
            case "Reamur":
                return input * 5 / 4;
            default:
                return input; // Jika skala asal adalah Celsius
        }
    }

    private double Fahrenheit(double input, String skalaAsal) {
        switch (skalaAsal) {
            case "Celsius":
                return input * 9 / 5 + 32;
            case "Kelvin":
                return (input - 32) * 5 / 9 + 273.15;
            case "Reamur":
                return (input - 32) * 4 / 9;
            default:
                return input; // Jika skala asal adalah Fahrenheit
        }
    }

    private double Kelvin(double input, String skalaAsal) {
        switch (skalaAsal) {
            case "Celsius":
                return input + 273.15;
            case "Fahrenheit":
                return (input - 273.15) * 9 / 5 + 32;
            case "Reamur":
                return (input - 273.15) * 4 / 5;
            default:
                return input; // Jika skala asal adalah Kelvin
        }
    }

    private double Reamur(double input, String skalaAsal) {
        switch (skalaAsal) {
            case "Celsius":
                return input * 4 / 5;
            case "Fahrenheit":
                return input * 9 / 4 + 32;
            case "Kelvin":
                return input * 5 / 4 + 273.15;
            default:
                return input; // Jika skala asal adalah Reamur
        }
    }
    
